// join()
var list = ["HTML5", "CSS3", "Javascript"];
console.log(list.join()); // Trả về: "HTML5,CSS3,Javascript"
console.log(list.join(",")); // Trả về: "HTML5,CSS3,Javascript"
console.log(list.join("")); // Trả về: "HTML5CSS3Javascript"
console.log(list.join(", ")); // Trả về: "HTML5, CSS3, Javascript"
console.log(list.join(" - ")); // Trả về: "HTML5 - CSS3 - Javascript"
console.log(list.join(" test "));
// end join()
